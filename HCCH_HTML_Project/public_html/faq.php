                      <h2 id="NavigatorHeader">Frequently Asked Questions</h2>  
                      <br />
                      <h3 id="FAQHeader">Who do you serve?
                      </h3>
                      <p>Orange Blossom Family Health(OBFH) provides health services 
                          for any low-income housed or homeless residents of Orange, 
                          Osceola and Seminole Counties.  
                      </p>    
                      <br/>
                      <h3 id="FAQHeader">What proof of income do you accept?
                      </h3>
                      <p>Pay stub, bank statement, unemployment letter with income, 
                          and/or SSI letter with income.
                      </p>
                      <br/>
                      <h3 id="FAQHeader">Where does your funding come from? 
                      </h3>
                      <p>We receive  government funding from the  federal, state, 
                          and local levels.  We also receive private financial 
                          and in-kind support to provide our many services. 
                      </p>
                      <br/>
                      <h3 id="FAQHeader">Who owns the Health Center?
                      </h3>
                      <p>Orange Blossom Family Health is operated by <a href="http://www.hcch.org"> 
                           The Health Care Center for the Homeless</a>, an independent 
                           501(c)(3) non-profit organization.  We are  governed 
                           by a Board of Directors comprised of community members 
                           and Health Center users. 
                      </p>
                      <br/>
                      <h3 id="FAQHeader">Do you accept private insurance?  
                      </h3>
                      <p>We accept some private insurances. Please call ahead to 
                          the location most convenient to you to check if we accept 
                          your private insurance.
                      </p>
                      <br/>
                      <h3 id="FAQHeader">Do you accept Medicaid and Medicare?
                      </h3>
                      <p>Yes, we do accept patients who are currently enrolled in 
                          Medicaid or Medicare.  Please bring  your card with you 
                          to your first visit. 
                      </p>
                      <br/>
                      <h3 id="FAQHeader">How do I make an appointment?
                      </h3>
                      <p>If this will be your first time coming to our Health Center, 
                          your initial visit will be as a walk-in patient.  If your 
                          provider here determines they need to see you again within 
                          an allotted time, the front desk will schedule your next 
                          appointment.
                      </p>
                      <br/>
                      <h3 id="FAQHeader">What should I bring with me on my first 
                          visit to the Health Center?
                      </h3>
                      <p>You should bring identification - this may be a driver's 
                          license, a state issued photo ID, or a birth certificate 
                          if you do not have a driver's license to verify your date 
                          of birth.  We will require proof of all household income.  
                          Please bring your Medicaid or Medicare card if currently 
                          enrolled. 
                      </p>
                      <br/>
                      <h3 id="FAQHeader">How do you charge for your services?
                      </h3>
                      <p>The cost of our services is determined by a sliding fee 
                          scale based on total household income, the number of 
                          persons living at your residence, and in accordance with 
                          the current Federal Poverty Guidelines.  The minimum 
                          charge for a medical visit is $10.00; for a dental visit 
                          - $10.00; for a behavioral health visit - $10.00; for 
                          pharmacy - $6.00 per prescription.  Please be aware these 
                          are minimum charges only and amounts will vary based on 
                          the service or services provided.  For patients unable 
                          to afford the charge for services provided, assistance 
                          is available.
                      </p>
                      <br/>
                      <h3 id="FAQHeader">Do you take walk-ins?
                      </h3>
                      <p>OBFH does accept walk-in patients in our medical clinic 
                          and they will be seen in order based on the urgency of  
                          their current illness or health issue.  For our dental 
                          clinic, walk-ins are seen on Mondays at 8:00 AM and 1:00 PM and 
                          Wednesdays at 8:00 AM and are  seen in the order they arrive.
                      </p>
